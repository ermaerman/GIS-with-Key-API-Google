  <h3 align="left"><font color="red"><b>Pesta Hadiah ASCO Daihatsu, Hadirkan Kegembiraan Bersama Konsumen</b></font></h3><br>
  <p>Kegembiraan menjadi milik ASCO Daihatsu bersama para konsumennya. Kebersamaan yang erat hadir saat digelar event 'Pesta Hadiah ASCO Daihatsu' yang berlangsung pada Sabtu, 25 Maret 2017. Seharian penuh event ini berlangsung di dealer ASCO Daihatsu Cabang Pasar Minggu, Jakarta dan Cabang Bekasi, Jawa Barat.
<br>
<br>

Sejak pagi konsumen yang diundangan khusus sudah hadir dengan antusias di kedua cabang tersebut. Cabang Bekasi menghadirkan konsumen dari Bekasi hingga Jakarta Timur. Sementara Cabang Pasar Minggu mengundang konsumen dari sejumlah wilayah Jakarta dan sekitarnya.


<br><br>

Seharian itu rangkaian acara digelar menarik mulai dari paket promo, aneka hadiah istimewa hingga service ditemani lantunan live musik.<br><br>


"Kami menghadirkan event spesial ini dengan banyak keuntungan untuk konsumen. Kami senang konsumen menyambut dengan baik dan bersuka cita di sini," kata Kepala Wilayah ASCO Daihatsu, Elbert Gozali.

<br><br>

Sejumlah promo dan paket menarik telah disiapkan untuk mereka yang akan membeli mobil. Yang spesial antara lain lucky draw untuk yang melakukan SPK pada hari H. Khusus bagi member AscoVIP juga mendapatkan voucher MAP Rp 500.000. Dan untuk pelanggan trade in menambahkan tambahan voucher pembelian Rp 1 juta.<br>
<br>

Bersamaan itu sejumlah leasing rekanan ASCO turut hadir dan siap memberikan promo DP dan bunga ringan. Antara lain ACC, Adira Finance, BCA Finance, Mandiri Utama Finance dan Daihatsu Finance.
<br>
<br>

Di lini service juga diberikan promo menarik. Pelanggan yang ingin service mobil mendapatkan diskon jasa 50% dan diskon oli 25%. Jika bengkel penuh, ASCO memberikan voucher diskon dengan masa berlaku 2 minggu.<br><br>


Tak cuma itu, gebyar hadiah menjadi pemikat bagi konsumen. Konsumen yang melakukan SPK kendaraan dan tanda jadi minimal Rp 1 juta mendapatkan satu kesempatan menarik lucy draw tersebut. Hadiahnya adalah TV 32 inci, kulkas 1 pintu, mesin cuci, home theater, oven microwave, rice cooker, karpet mobil exclusive, voucher service Rp 300.000 hingga voucher oli motul Rp 200.000. Ada pula foto selfie kreatif bersama maskot ASCO berhadiah menarik.
<br><br>



Kebersamaan ini juga diisi kegiatan test ride untuk seluruh line-up Daihatsu. "Kami menyambut baik antusiasme konsumen. Benefit yang kami berikan di event ini sangat memuaskan. Kami rutin menghadirkan event seperti ini dan akan terus menggelarnya demi memuaskan pelanggan setia kami," ujar Audy The Khe Po Managing Director ASCO Daihatsu.</p>
  
